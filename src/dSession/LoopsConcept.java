package dSession;



public class LoopsConcept {

	public static void main(String[] args) {
		
		
		// 1. While loop
		
		int i=0;
		
		while (i<=10) {
			
			System.out.println(i);
			
//			if (i%2==0)
//			{
//				System.out.println("add");
//			}
			i++;
			
		}
		
		// for loop
		
		System.out.println("=============");
		
	for (int j = 0; j < 10; j++) {
	
		System.out.println(j);
	}
	
	System.out.println("=============");
	
	for (int k = 10; k >= 0 ; k--) {
		System.out.println(k);
		
	}
	
	System.out.println("=============");
	
	
	int p =1;
	do {
		System.out.println(p);
		
		
	} while (p<=10);
		
		
	}

}
